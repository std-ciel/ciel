    case TokenType::LT_OP:
        return Token::LT_OP;
    case TokenType::LE_OP:
        return Token::LE_OP;
    case TokenType::GT_OP:
        return Token::GT_OP;
    case TokenType::GE_OP:
        return Token::GE_OP;
    case TokenType::EQ_OP:
