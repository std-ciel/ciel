    case TokenType::UNSIGNED:
        return Token::UNSIGNED;
    case TokenType::CHAR:
        return Token::CHAR;
    case TokenType::BOOL:
        return Token::BOOL;
    case TokenType::FLOAT:
        return Token::FLOAT;
    case TokenType::VOID:
        return Token::VOID;
    case TokenType::INT_LITERAL:
        return Token::INT_LITERAL;
    case TokenType::CHAR_LITERAL:
        return Token::CHAR_LITERAL;
    case TokenType::STRING_LITERAL:
        return Token::STRING_LITERAL;
    case TokenType::BOOL_LITERAL:
        return Token::BOOL_LITERAL;
    case TokenType::FLOAT_LITERAL:
        return Token::FLOAT_LITERAL;
    case TokenType::IDENTIFIER:
        return Token::IDENTIFIER;
    case TokenType::TYPENAME:
        return Token::TYPE_NAME;
    case TokenType::RETURN:
        return Token::RETURN;
    case TokenType::IF:
        return Token::IF;
    case TokenType::BREAK:
        return Token::BREAK;
    case TokenType::CONTINUE:
        return Token::CONTINUE;
    case TokenType::GOTO:
        return Token::GOTO;
    case TokenType::ELSE:
        return Token::ELSE;
    case TokenType::SWITCH:
        return Token::SWITCH;
    case TokenType::CASE:
        return Token::CASE;
    case TokenType::DEFAULT:
        return Token::DEFAULT;
    case TokenType::FOR:
        return Token::FOR;
    case TokenType::DO:
        return Token::DO;
    case TokenType::WHILE:
        return Token::WHILE;
    case TokenType::UNTIL:
        return Token::UNTIL;
    case TokenType::STATIC:
        return Token::STATIC;
    case TokenType::CONST:
        return Token::CONST;
    case TokenType::VOLATILE:
        return Token::VOLATILE;
    case TokenType::ENUM:
        return Token::ENUM;
    case TokenType::STRUCT:
        return Token::STRUCT;
    case TokenType::UNION:
        return Token::UNION;
    case TokenType::TYPEDEF:
        return Token::TYPEDEF;
    case TokenType::CLASS:
        return Token::CLASS;
    case TokenType::PUBLIC:
        return Token::PUBLIC;
    case TokenType::PRIVATE:
        return Token::PRIVATE;
    case TokenType::PROTECTED:
        return Token::PROTECTED;
    case TokenType::OPERATOR:
        return Token::OPERATOR;
    case TokenType::NEW:
        return Token::NEW;
    case TokenType::DELETE:
        return Token::DELETE;
    case TokenType::THIS:
        return Token::THIS;
    case TokenType::SIZEOF:
        return Token::SIZEOF;
    case TokenType::ARROW_OP:
        return Token::ARROW_OP;
    case TokenType::DOT_OP:
        return Token::DOT_OP;
    case TokenType::QUESTION_OP:
        return Token::QUESTION_OP;
    case TokenType::ELLIPSIS_OP:
        return Token::ELLIPSIS_OP;

    case TokenType::ASSIGN_OP:
        return Token::ASSIGN_OP;
    case TokenType::PLUS_ASSIGN_OP:
        return Token::PLUS_ASSIGN_OP;
    case TokenType::MINUS_ASSIGN_OP:
        return Token::MINUS_ASSIGN_OP;
    case TokenType::STAR_ASSIGN_OP:
        return Token::STAR_ASSIGN_OP;
    case TokenType::DIVIDE_ASSIGN_OP:
